function escapeRegExp(str: string): string {
  return str.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

export function validateReview(review: string): {
  isClean: boolean;
} {
  const text = review.trim().toLowerCase();
  const found = badWordsData.badWords.filter((word) => {
    const pattern = `(?<!\\p{L})${escapeRegExp(word)}(?!\\p{L})`;
    return new RegExp(pattern, "iu").test(text);
  });
  return {
    isClean: found.length === 0,
  };
}
